            textOtros.setText(datos.getOtrosDatosInteres());
            textPatologia.setText(datos.getPatologias());
            textTabaco.setText(datos.getConsumoTabaco());
            textOcupacion.setText(datos.getOcupacion());
            comerFuera.setSelected(datos.isCocinaEncasa());
            deportista.setSelected(datos.isDeportista());
            actividad.setSelectedItem(datos.getActividadFisica());
            
