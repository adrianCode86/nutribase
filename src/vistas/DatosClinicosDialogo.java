    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
       String alcohol = textAlcohol.getText();
       String buenos = textBuenos.getText();
       String malos = textMalos.getText();
       String medicacion = textMedicacion.getText();
       String ocupacion = textOcupacion.getText();
       String otros = textOtros.getText();
       String patologia = textPatologia.getText();
       String tabaco = textTabaco.getText();
       String acti = actividad.getSelectedItem().toString();
       
       if(!alcohol.equalsIgnoreCase("")){
           datos.setConsumoAlcohol(alcohol);
       }
       if(!buenos.equalsIgnoreCase("")){
           datos.setGustosBuenos(buenos);
       }
       if(!malos.equalsIgnoreCase("")){
           datos.setGustosMalos(malos);
       }
       if(!medicacion.equalsIgnoreCase("")){
           datos.setMedicacion(medicacion);
       }
       if(!ocupacion.equalsIgnoreCase("")){
           datos.setOcupacion(ocupacion);
       }
       if(!otros.equalsIgnoreCase("")){
           datos.setOtrosDatosInteres(otros);
       }
       if(!patologia.equalsIgnoreCase("")){
           datos.setPatologias(patologia);
       }
       if(!tabaco.equalsIgnoreCase("")){
           datos.setConsumoTabaco(tabaco);
       }
       if(!acti.equalsIgnoreCase("")){
           datos.setActividadFisica(acti);
       }
       datos.setDeportista(deportista.isSelected());
       datos.setCocinaEncasa(comerFuera.isSelected());
       
       if(actualizar){
           op.actualizarDatosClinicos(datos);
       }else{
           datos.setPaciente(paciente);
           paciente.setDatosClinicos(datos);
           op.actulizarPaciente(paciente);
       }
       dispose();
    }//GEN-LAST:event_botonAceptarActionPerformed

