    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        String abdominal = textAbdominal.getText();
        String pierna = textPierna.getText();
        String muslo = textMuslo.getText();
        String subescapular = textSubescapular.getText();
        String suprailiaco = textSuprailiaco.getText();
        String tricipital = textTricipital.getText();

        if (!abdominal.equalsIgnoreCase("")) {
            pliegue.setAbdominal(Float.parseFloat(abdominal));
        }
        if (!pierna.equalsIgnoreCase("")) {
            pliegue.setMedidaPlieguePierna((Float.parseFloat(pierna)));
        }
        if (!muslo.equalsIgnoreCase("")) {
            pliegue.setMuslo(Float.parseFloat(muslo));
        }
        if (!subescapular.equalsIgnoreCase("")) {
            pliegue.setSubescapular(Float.parseFloat(subescapular));
        }
        if (!suprailiaco.equalsIgnoreCase("")) {
            pliegue.setSuprailiaco(Float.parseFloat(suprailiaco));
        }
        if (!tricipital.equalsIgnoreCase("")) {
            pliegue.setTricipital(Float.parseFloat(tricipital));
        }

        if (actualizar) {
            op.actulizarMedidasPliegue(pliegue);
        } else {
            pliegue.setFecha(new Date());
            pliegue.setPaciente(paciente);
            op.insertarMedidasPliegue(pliegue);
        }
        if (tabla != null) {
            tabla.rellenarTabla();
        }
        dispose();
    }//GEN-LAST:event_botonAceptarActionPerformed

