    private void rellenarCampos() {
        if (pliegue != null && actualizar) {
            textAbdominal.setText(String.valueOf(pliegue.getAbdominal()));
            textPierna.setText(String.valueOf(pliegue.getMedidaPlieguePierna()));
            textMuslo.setText(String.valueOf(pliegue.getMuslo()));
            textSubescapular.setText(String.valueOf(pliegue.getSubescapular()));
            textSuprailiaco.setText(String.valueOf(pliegue.getSuprailiaco()));
            textTricipital.setText(String.valueOf(pliegue.getTricipital()));

            textAbdominal.setEditable(false);
            textPierna.setEditable(false);
            textPierna.setEditable(false);
            textMuslo.setEditable(false);
            textSubescapular.setEditable(false);
            textSuprailiaco.setEditable(false);
            textTricipital.setEditable(false);
            botonAceptar.setVisible(false);

        }
    }

