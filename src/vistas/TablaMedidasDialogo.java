    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
         int posicion=tabla.getSelectedRow();
       if(evt.getClickCount()==2){
       if(posicion !=-1){
          
          Date aux=medidas.get(posicion).getFecha();
           MedidasNormales medida=op.getMedidasNormales(aux, dni);
           MedidasNormalesDialogo vista;
           try {
               
               vista = new MedidasNormalesDialogo(parent, rootPaneCheckingEnabled,medida );
               vista.setVisible(rootPaneCheckingEnabled);
               rellernarTabla(dni);
               
           } catch (UnsupportedLookAndFeelException ex) {
               Logger.getLogger(TablaPacientes.class.getName()).log(Level.SEVERE, null, ex);
           }
           
        }
       }
    }//GEN-LAST:event_tablaMouseClicked

    private void menuBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuBorrarActionPerformed
        int posicion=tabla.getSelectedRow();
        if(posicion != -1){
           MedidasNormales medida=op.getMedidasNormales(medidas.get(posicion).getFecha(), dni);
           int confirmar=JOptionPane.showConfirmDialog(null, "¿Está seguro de borra?");
           if(confirmar==0){
           op.borrarMedidasNormales(medida);
           }
            rellernarTabla(dni);
        }
    }//GEN-LAST:event_menuBorrarActionPerformed

