     public void rellernarTabla(String dni){
         medidas= op.obtenerMedidasNormalesTabla(dni);
         TablaModeloNoEdit model=new TablaModeloNoEdit();
         model.setColumnIdentifiers(new String[]{"Fecha","Peso","Grasa %","Cintura","Abdomen","Cadera","Muslo"});
         for(MedidasNormalesTabla medida:medidas){
             model.addRow(medida.toObject());
         }
         tabla.setModel(model);
         
     }
